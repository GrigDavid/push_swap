#include "push_swap.h"

void	ft_stkiter(t_stack *lst, void (*f)(int))
{
	t_stack	*p;

	p = lst;
	while (p && f)
	{
		f(p->content);
		write(1, " ", 1);
		p = p->next;
	}
}
