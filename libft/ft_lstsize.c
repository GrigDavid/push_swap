#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		i;
	t_list	*p;

	p = lst;
	i = 1;
	if (!lst)
		return (0);
	while (p->next != 0)
	{
		i++;
		p = p->next;
	}
	return (i);
}
