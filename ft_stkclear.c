#include "push_swap.h"

void	ft_stkclear(t_stack **lst)
{
	t_stack	*p;

	if (!*lst || !lst)
		return ;
	while (*lst)
	{
		p = (*lst)->next;
		free(*lst);
		*lst = p;
	}
}
